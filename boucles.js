//Boucle for, on connaît le nombre de tour à l'avance
console.log("Boucle for");
for(let i=0;i<=10;i++){
    console.log(i);
}

//Boucle while avec le test en début de boucle 
console.log("Boucle while");
let i=0;
while (i<=10){
    console.log(i);
    i++;
}

//Boucle do,while avec le test en fin de boucle, passe au moins une fois dans la boucle 
console.log("Boucle do/while");
i=0;
do{
    console.log(i);
    i++;
} while(i<=10);

